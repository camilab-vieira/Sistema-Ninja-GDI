-- CRIA TABELA PESSOA
CREATE TABLE PESSOA(
CADASTRO NUMBER(5),
NOME VARCHAR(80) CONSTRAINT NN_PESSOA_NOME NOT NULL,
DATA_NAS DATE NOT NULL,
IDADE NUMBER(2),
CONSTRAINT PK_PESSOA PRIMARY KEY (CADASTRO)
);

-- CRIA TABELA NINJA
CREATE TABLE NINJA(
CADASTRO NUMBER(5) PRIMARY KEY,
REGISTRO_NINJA NUMBER(5) NOT NULL UNIQUE,
CONSTRAINT FK_NINJA_PESSOA FOREIGN KEY (CADASTRO) REFERENCES PESSOA (CADASTRO)
);

-- CRIA TABELA PAIS
CREATE TABLE PAIS(
ID NUMBER(5) PRIMARY KEY,
NOME VARCHAR(30) 
);

-- CRIA TABELA VILA
CREATE TABLE VILA(
ID NUMBER(5) PRIMARY KEY,    
NOME VARCHAR(30),
CONSTRAINT FK_VILA_PAIS FOREIGN KEY (ID) REFERENCES PAIS (ID)
);

-- CRIA TABELA KAGE
CREATE TABLE KAGE(
CADASTRO NUMBER(5) PRIMARY KEY,
ID_VILA NUMBER(5),
CONSTRAINT FK_KAGE_PESSOA FOREIGN KEY (CADASTRO) REFERENCES PESSOA (CADASTRO),
CONSTRAINT FK_KAGE_VILA FOREIGN KEY (ID_VILA) REFERENCES PAIS (ID)
);

-- CRIA TABELA MISSÃO
CREATE TABLE MISSAO(
CODIGO NUMBER(5) PRIMARY KEY,
DESCRICAO VARCHAR(200),
DIFICULDADE VARCHAR(1) NOT NULL,
VALOR NUMBER(15) NOT NULL,
DATA_INICIO DATE,
DATA_FIM DATE,
DATA_SOLICITACAO DATE NOT NULL,
CADASTRO NUMBER(5),
CONSTRAINT FK_MISSAO_PESSOA FOREIGN KEY (CADASTRO) REFERENCES PESSOA (CADASTRO)
);

-- CRIA TABELA RECURSOS
CREATE TABLE RECURSOS (
CODIGO NUMBER(5) NOT NULL,
RECURSO VARCHAR(80), 
CONSTRAINT FK_RECURSO_MISSAO FOREIGN KEY (CODIGO) REFERENCES MISSAO (CODIGO),
CONSTRAINT PK_RECURSO PRIMARY KEY (CODIGO, RECURSO)
);   	 
       	
-- CRIA TABELA HABILIDADE
CREATE TABLE HABILIDADE (
ID NUMBER(5) PRIMARY KEY,
TIPO VARCHAR(30)
);

-- CRIA TABELA EQUIPAMENTO
CREATE TABLE EQUIPAMENTO (
CODIGO NUMBER(5) PRIMARY KEY,
TIPO VARCHAR(20)
);

-- CRIA TABELA LOJA
CREATE TABLE LOJA(
ID NUMBER(5) PRIMARY KEY,
NOME VARCHAR(30)
);

-- CRIA TABELA TIME
CREATE TABLE TIME(
NUMERO_DO_TIME NUMBER(3) PRIMARY KEY, 
CADASTRO NUMBER(5) NOT NULL UNIQUE,
CONSTRAINT FK_TIME_NINJA FOREIGN KEY (CADASTRO) REFERENCES NINJA (CADASTRO)
);

-- CRIA TABELA TIME_FAZ_MISSAO
CREATE TABLE TIME_FAZ_MISSAO(
NUMERO_DO_TIME NUMBER(3) PRIMARY KEY, 
CODIGO NUMBER(5),
CONSTRAINT FK_TIME_FAZ_MISSAO_MISSAO FOREIGN KEY (CODIGO) REFERENCES MISSAO (CODIGO),
CONSTRAINT FK_TIME_FAZ_MISSAO_TIME FOREIGN KEY (NUMERO_DO_TIME) REFERENCES TIME (NUMERO_DO_TIME)
);

-- CRIA TABELA NINJA_TREINA_NINJA
CREATE TABLE NINJA_TREINA_NINJA(
CADASTRO_NINJA_TREINADOR NUMBER(5), 
CADASTRO_NINJA_TREINADO NUMBER(5),
CONSTRAINT FK_NINJA_TREINA_NINJA_TREINADOR FOREIGN KEY (CADASTRO_NINJA_TREINADOR) REFERENCES NINJA (CADASTRO),
CONSTRAINT FK_NINJA_TREINA_NINJA_TREINADO FOREIGN KEY (CADASTRO_NINJA_TREINADO) REFERENCES NINJA (CADASTRO),
CONSTRAINT PK_NINJA_TREINA_NINJA PRIMARY KEY (CADASTRO_NINJA_TREINADOR, CADASTRO_NINJA_TREINADO)
);

-- CRIA TABELA NINJA_TEM_HABILIDADE
CREATE TABLE NINJA_TEM_HABILIDADE(
CADASTRO NUMBER(5),
ID NUMBER(5),
CONSTRAINT FK_NINJA_TEM_HABILIDADE_NINJA FOREIGN KEY (CADASTRO) REFERENCES NINJA (CADASTRO),
CONSTRAINT FK_NINJA_TEM_HABILIDADE_HABILIDADE FOREIGN KEY (ID) REFERENCES HABILIDADE (ID),
CONSTRAINT PK_NINJA_TEM_HABILIDADE PRIMARY KEY (CADASTRO, ID)
);

-- CRIA TABELA TEM_USA_EQUIPAMENTO
CREATE TABLE TEM_USA_EQUIPAMENTO(
CODIGO NUMBER(5),
ID NUMBER(5),
CADASTRO NUMBER(5),
CONSTRAINT FK_TEM_USA_EQUIPAMENTO_EQUIPAMENTO FOREIGN KEY (CODIGO) REFERENCES EQUIPAMENTO (CODIGO),
CONSTRAINT FK_TEM_USA_EQUIPAMENTO_HABILIDADE FOREIGN KEY (ID) REFERENCES HABILIDADE (ID),
CONSTRAINT FK_TEM_USA_EQUIPAMENTO_NINJA FOREIGN KEY (CADASTRO) REFERENCES NINJA (CADASTRO),
CONSTRAINT PK_TEM_USA_EQUIPAMENTO PRIMARY KEY (CODIGO, CADASTRO, ID)
);

-- CRIA TABELA NINJA_COMPRA_NA_LOJA_EQUIPAMENTO
CREATE TABLE NINJA_COMPRA_NA_LOJA_EQUIPAMENTO(
CODIGO NUMBER(5),
ID NUMBER(5),
CADASTRO NUMBER(5),
CONSTRAINT FK_NINJA_COMPRA_NA_LOJA_EQUIPAMENTO_EQUIPAMENTO FOREIGN KEY (CODIGO) REFERENCES EQUIPAMENTO (CODIGO),
CONSTRAINT FK_NINJA_COMPRA_NA_LOJA_EQUIPAMENTO_HABILIDADE FOREIGN KEY (ID) REFERENCES LOJA (ID),
CONSTRAINT FK_NINJA_COMPRA_NA_LOJA_EQUIPAMENTO_NOME FOREIGN KEY (CADASTRO) REFERENCES NINJA (CADASTRO),
CONSTRAINT PK_NINJA_COMPRA_NA_LOJA_EQUIPAMENTO PRIMARY KEY (CODIGO, CADASTRO, ID)
);

-- CRIA TABELA NINJA_PARTICIPA_TIME
CREATE TABLE NINJA_PARTICIPA_TIME(
CADASTRO NUMBER(5),
NUMERO_DO_TIME NUMBER(3), 
DATA DATE,
TIPO_IO NUMBER(1) NOT NULL,
CONSTRAINT FK_NINJA_PARTICIPA_TIME_NINJA FOREIGN KEY (CADASTRO) REFERENCES NINJA (CADASTRO),
CONSTRAINT FK_NINJA_PARTICIPA_TIME_TIME FOREIGN KEY (NUMERO_DO_TIME) REFERENCES TIME (NUMERO_DO_TIME),
CONSTRAINT PK_NINJA_PARTICIPA_TIME PRIMARY KEY (CADASTRO, NUMERO_DO_TIME, DATA)
);

